import q150 from './';
describe('150. 逆波兰表达式求值', () => {
  test('["2", "1", "+", "3", "*"]', () => {
    expect(q150(['2', '1', '+', '3', '*'])).toBe(9);
  });
  test('["4", "13", "5", "/", "+"]', () => {
    expect(q150(['4', '13', '5', '/', '+'])).toBe(6);
  });
  test('["10", "6", "9", "3", "+", "-11", "*", "/", "*", "17", "+", "5", "+"]', () => {
    expect(q150(['10', '6', '9', '3', '+', '-11', '*', '/', '*', '17', '+', '5', '+'])).toBe(22);
  });
  test('["4","-2","/","2","-3","-","-"]', () => {
    expect(q150(["4","-2","/","2","-3","-","-"])).toBe(-7);
  });
});
